'''async
The async keyword in Dart is used to define an asynchronous function. 
An asynchronous function is a function that performs its work in the background and returns a Future. 
This allows your program to continue executing other code while waiting for the asynchronous operation to complete, rather than blocking and waiting.
'''

'''Future object (follows async command)
An async function always returns a Future, even if you do not explicitly return a value. 
A Future represents a value that will be available at some point in the future. 
It is a placeholder for a value that might not be immediately available.
'''

'''await
async keyword ask the program to run other codes when waiting for the command, while await asks the program
to wait for the end of async function before going further.
'''